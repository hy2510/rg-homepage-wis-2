function getReadingUnitImage(
  point,
  earnPoint,
) {
  const beforePoint = point - earnPoint

  const beforeIndex = findReadingUnitImageIndex(beforePoint)
  const newIndex = findReadingUnitImageIndex(point)

  if (beforeIndex < newIndex) {
    return UNIT_STORY_DATA[newIndex]
  }
  return undefined
}

function findReadingUnitImageIndex(point) {
  let findIndex = -1
  for (let i = 0; i < UNIT_STORY_DATA.length - 1; i++) {
    const unit = UNIT_STORY_DATA[i]
    const nextUnit = UNIT_STORY_DATA[i + 1]
    if (unit.point <= point && point < nextUnit.point) {
      findIndex = i
      break
    }
  }
  return findIndex
}

function findResultReadingUnit(level) {
  const lv = level.toUpperCase()
  let unit = 'baro'
  switch (lv) {
    case 'KA':
      unit = 'baro'
      break
    case 'KB':
      unit = 'chello'
      break
    case 'KC':
      unit = 'millo'
      break
    case '1A':
      unit = 'jack'
      break
    case '1B':
      unit = 'sheila'
      break
    case '1C':
      unit = 'blanc'
      break
    case '2A':
      unit = 'green_thumb'
      break
    case '2B':
      unit = 'tori'
      break
    case '2C':
      unit = 'roro'
      break
    case '3A':
      unit = 'edmond'
      break
    case '3B':
      unit = 'leoni'
      break
    case '3C':
      unit = 'goma'
      break
    case '4A':
    case '4B':
    case '4C':
      unit = 'gino'
      break
    case '5A':
    case '5B':
    case '5C':
    case '6A':
    case '6B':
    case '6C':
      unit = 'dodo'
      break
  }
  return unit
}
